#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*str;
	size_t			count;

	count = 0;
	str = (unsigned char *)s;
	while (count < n && str[count] != '\0')
	{
		str[count] = '\0';
		count++;
	}
}
